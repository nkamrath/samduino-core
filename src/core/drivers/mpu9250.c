mpu9250_t Mpu9250_Create(mpu9250_params_t* params)
{
	//first create the underlying spi bus
	_device.spi_bus = Spi_Create(&params->spi_params);

	if(_device.spi_bus)
	{
		_device.cs_pin = Pin_Create(&params->cs_pin_params);
        _device.int_pin = Pin_Create(&params->int_pin_params);


        _HardReset(&_device);

        //need delay here for hard reset to clear
        volatile int delay_counter = 2200000;
        for(volatile int i = 0; i < delay_counter; i++);
        _WakeChip(&_device);

		bool res = _CheckWhoAmI(&_device);
		if(res == false)
		{
			return NULL;
		}

        _DisableAllSensors(&_device);

		res = _LoadDmpFirmware(&_device);
		if(res == false)
		{
			return NULL;
		}

        Mpu9250_SetGyroScale(&_device, params->gyro_scale);
        Mpu9250_SetAccelScale(&_device, params->accel_scale);
        _SetSampleRate(&_device, 200);
        _EnableFifo(&_device);

        _EnableAllSensors(&_device);

		return &_device;
	}

	return NULL;
}

