static uint8_t dummy_buffer[64];

static _mpu9250_t _device;

void _Write(_mpu9250_t* device, uint8_t address, void* data, uint8_t data_length)
{
	uint8_t write_address = address;

	Pin_Clear(device->cs_pin);
	Spi_DmaTransfer(device->spi_bus, &write_address, data, &write_address, dummy_buffer, 1, data_length);
	Pin_Set(device->cs_pin);
}

void _Read(_mpu9250_t* device, uint8_t address, void* data, uint8_t data_length)
{
	uint8_t read_address = address | 0x80; //msb is read bit set for read

	Pin_Clear(device->cs_pin);
	Spi_DmaTransfer(device->spi_bus, &read_address, dummy_buffer, &read_address, data, 1, data_length);
	Pin_Set(device->cs_pin);
}

void _WriteDmpMem(_mpu9250_t* device, uint16_t address, void* data, uint8_t data_length)
{
    uint8_t tmp[2];
    tmp[0] = (unsigned char)((address >> 8) & 0xff);
    tmp[1] = (unsigned char)(address & 0xFF);
    _Write(device, MEM_BANK_SEL_ADDR, tmp, 2);
    _Write(device, MEM_R_W_ADDR, data, data_length);
}

void _HardReset(_mpu9250_t* device)
{
    uint8_t reset_val = 0x80;
    _Write(device, PWR_MGMT_1, &reset_val, 1);
}

void _WakeChip(_mpu9250_t* device)
{
    uint8_t wake_val = 0x00;
    //_Write(device, PWR_MGMT_1, &wake_val, 1);
    _Write(device, PWR_MGMT_2, &wake_val, 1);
}

void _EnableDmp(_mpu9250_t* device)
{
    uint8_t enable_val = 0x80 | (1<<6) | (1<<2) | (1<<5);
    _Write(device, USER_CTRL, &enable_val, 1);
}

bool _SetupMag(_mpu9250_t* device)
{
    //enable i2c master mode
    uint8_t enable_val = (1<<5);
    _Write(device, USER_CTRL, &enable_val, 1);

    enable_val = I2C_MASTER_CONTROL_VALUE;
    _Write(device, I2C_MASTER_CONTROL, &enable_val, 1);

    //this is a magici write to an undocumented register...
    uint8_t write_val = AK8963_ADDRESS;
    _Write(device, I2C_SLV0_ADDR, &write_val, 1);

    write_val = 0x0B;
    _Write(device, I2C_SLV0_REG, &write_val, 1);

    write_val = 0x01;
    _Write(device, I2C_SLV0_DO, &write_val, 1);

    write_val = 0x81;
    _Write(device, I2C_SLV0_CTRL, &write_val, 1);

    //need delay for mpu to actually push this write to the mag
    for(volatile int i = 0; i < 1000000; i++);

    //need to enable continuous measurement mode
    write_val = AK8963_ADDRESS;
    _Write(device, I2C_SLV0_ADDR, &write_val, 1);

    write_val = 0x0A;
    _Write(device, I2C_SLV0_REG, &write_val, 1);

    write_val = 0x16;
    _Write(device, I2C_SLV0_DO, &write_val, 1);

    write_val = 0x81;
    _Write(device, I2C_SLV0_CTRL, &write_val, 1);

    for(volatile int i = 0; i < 1000000; i++);

    //now setup the final values that will stay in the mpu slv0 regs to continuously read the mag value
    //seupt the slave i2c address and or with 0x80 to indicate read
    write_val = AK8963_ADDRESS | 0x80;
    _Write(device, I2C_SLV0_ADDR, &write_val, 1);
    
    //set the register of the i2c slave to read
    write_val = 0x02;
    _Write(device, I2C_SLV0_REG, &write_val, 1);

    write_val = 0x88;
    _Write(device, I2C_SLV0_CTRL, &write_val, 1);

    return true;
}



bool _LoadDmpFirmware(_mpu9250_t* device)
{
	unsigned char tmp[2];

    uint16_t start_address = 0x0400;
    tmp[0] = start_address >> 8;
    tmp[1] = start_address & 0xFF;
    
    _Write(device, PRGM_START_H_ADDR, tmp, 2);

	for(int i = 0; i < DMP_CODE_SIZE; i++)
	{
	    tmp[0] = (unsigned char)((i >> 8) & 0xff);
	    tmp[1] = (unsigned char)(i & 0xFF);

	    uint8_t fw_byte = _dmp_binary[i];
	    // _Write(device, MEM_BANK_SEL_ADDR, tmp, 2);
	    // _Write(device, MEM_R_W_ADDR, &fw_byte, 1);
        _WriteDmpMem(device, i, &fw_byte, 1);

	    fw_byte = 0;
	    tmp[0] = (unsigned char)((i >> 8) & 0xff);
	    tmp[1] = (unsigned char)(i & 0xFF);
	    _Write(device, MEM_BANK_SEL_ADDR, tmp, 2);
	    _Read(device, MEM_R_W_ADDR, &fw_byte, 1);
	    if(fw_byte != _dmp_binary[i])
	    {
	    	return false;
	    }

	}

	tmp[0] = start_address >> 8;
	tmp[1] = start_address & 0xFF;
	
	_Write(device, PRGM_START_H_ADDR, tmp, 2);

    //setup dmp sample rate
    uint16_t dmp_div = 1;//DMP_SAMPLE_RATE / rate - 1;
    tmp[0] = (unsigned char)((dmp_div >> 8) & 0xFF);
    tmp[1] = (unsigned char)(dmp_div & 0xFF);
    _WriteDmpMem(device, D_0_22, tmp, 2);

    unsigned char regs[4];
    // regs[0] = DINBC0;
    // regs[1] = DINBC2;
    // regs[2] = DINBC4;
    // regs[3] = DINBC6;
    //_WriteDmpMem(device, CFG_LP_QUAT, regs, 4);

    regs[0] = DINA20;
    regs[1] = DINA28;
    regs[2] = DINA30;
    regs[3] = DINA38;

    _WriteDmpMem(device, CFG_8, regs, 4);

	return true;
}

